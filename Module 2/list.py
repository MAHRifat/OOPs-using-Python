# list/array/collection is same  

numbers =[3984,34,3,4,23,43,4,5,5,46,25623,5]

print(numbers[3])
print(numbers[-7])

print(numbers[3:7])  #list(start:end)  -> start from start index and stops before end index

print(numbers[3:9:2]) #list(start:end:step)

print(numbers[7:3:-2])
print(numbers[3:])
print(numbers[:6])
print(numbers[:])  # shortcut to copy a list
print(numbers[::-1]) # shortcut to reverse a list

